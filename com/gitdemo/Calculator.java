package com.gitdemo;

public class Calculator {

    public Calculator() {
    }

    /**
     * Add two numbers
     */
    public int add(int a, int b) {
        // Add two numbers
        return a + b;
    }

    /**
     * Subtract two numbers
     */
    public int subtract(int a, int b) {
        return a - b;
    }

}